/*
    Stack Data Structure:
        - LIFO (Last In First Out) or FILO (First In Last Out)
        - the element that is added last is the first one to be removed
        - every element we push into the stack is placed on top of the previous element
        - every element we pop from the stack is removed from the top of the stack
        - the last element added to the stack will be the first element removed from the stack
        - the first element added to the stack will be the last element removed from the stack
        - operations
            - push: add an element to the top of the stack
            - pop: remove an element from the top of the stack
            - peek: get the top element of the stack
            - isEmpty: check if the stack is empty
            - size: get the number of elements in the stack
*/

